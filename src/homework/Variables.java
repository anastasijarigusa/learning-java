package homework;

public class Variables {
    public static void main(String[] args) {

        //Data type - byte
        byte yo = 34; //Person's age
        byte c = -8; //Temperature in Celsius on Earth
            System.out.println(yo);
            System.out.println(c);

        //Data type - short
        short books = 459; //Number of books in library
        short stud = 17548; //Total number of students in university
            System.out.println(books);
            System.out.println(stud);

        //Data type - int
        int num = 22334455; //Mobile number
        int vm = 354912; //Vehicle mileage
            System.out.println(num);
            System.out.println(vm);

        //Data type - long
        long pas = 112233445566778899L; //Password
        long am = 85471125L; //Total companies amount of income
            System.out.println(pas);
            System.out.println(am);

        //Data type - float
        float avg = 8.3F; //Average score among all students
        float price = 15.99F; //Cake price
            System.out.println(avg);
            System.out.println(price);

        //Data type - double
        double res = 3.6666666666; //Result of the equation
        double ap = 45.40045; //Apartment area m^2
            System.out.println(res);
            System.out.println(ap);

        //Data type - boolean
        boolean snow = false; //Snow falls in summer
        boolean water = true; //Water is liquid
            System.out.println(snow);
            System.out.println(water);

        //Data type - char
        char N = 78; //Letter "N"
        char n = 110; //Letter "n"
            System.out.println(N);
            System.out.println(n);
    }
}